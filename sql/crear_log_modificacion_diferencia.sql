CREATE TABLE INV_DIFERENCIA_LOG_TBL (
    LOG_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DIFERENCIA_ID NUMBER NOT NULL,
    NUMERO_CONTEO VARCHAR2(100) NOT NULL,
    ALMACEN VARCHAR2(50) NOT NULL,
    NOMBRE_USUARIO VARCHAR2(200) NOT NULL,
    CEDULA_USUARIO VARCHAR2(30) NOT NULL,
    PROCESO VARCHAR2(80) NOT NULL,
    CODIGO_SAP VARCHAR2(100) NOT NULL,
    DIFERENCIA_ANTERIOR NUMBER(18, 4),
    DIFERENCIA_NUEVA NUMBER(18, 4),
    EAN VARCHAR2(100),
    FECHA_MODIFICACION TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE INDEX IDX_INV_DIF_LOG_CONTEO
    ON INV_DIFERENCIA_LOG_TBL (NUMERO_CONTEO, ALMACEN);

CREATE INDEX IDX_INV_DIF_LOG_DIFERENCIA
    ON INV_DIFERENCIA_LOG_TBL (DIFERENCIA_ID);

COMMENT ON TABLE INV_DIFERENCIA_LOG_TBL IS
    'Auditoria de modificaciones manuales a diferencias del segundo conteo';

COMMENT ON COLUMN INV_DIFERENCIA_LOG_TBL.PROCESO IS
    'Proceso que origino el cambio, por ejemplo MODIFICACION_SEGUNDO_CONTEO_JEFE';
